USE empresadb;

CREATE TABLE departamento
(
    id_departamento INT PRIMARY KEY,
    nombre          VARCHAR(75)    NOT NULL UNIQUE,
    presupuesto     DECIMAL(15, 2) NOT NULL
        CHECK ( presupuesto >= 0 )
);

CREATE TABLE puesto
(
    id_puesto          INT PRIMARY KEY AUTO_INCREMENT,
    nombre             VARCHAR(50) NOT NULL UNIQUE,
    fk_id_departamento INT         NOT NULL,
    FOREIGN KEY (fk_id_departamento)
        REFERENCES departamento (id_departamento)
);

CREATE TABLE empleado
(
    cui_empleado         BIGINT PRIMARY KEY,
    nombre               VARCHAR(100)   NOT NULL,
    apellidos            VARCHAR(125)   NOT NULL,
    telefono             INT,
    correo               VARCHAR(50),
    usuario              VARCHAR(50)    NOT NULL,
    contrasenya          VARCHAR(255)   NOT NULL,
    fk_id_puesto         INT            NOT NULL,
    salario              DECIMAL(12, 2) NOT NULL,
    fecha_ultimo_aumento DATE,
    fecha_ingreso        DATE           NOT NULL,
    fecha_de_baja        DATE,
    FOREIGN KEY (fk_id_puesto)
        REFERENCES puesto (id_puesto)
);

CREATE TABLE aumento_usuario
(
    id_aumento_usuario   INT PRIMARY KEY AUTO_INCREMENT,
    fk_cui_empleado      BIGINT         NOT NULL,
    salario              DECIMAL(15, 2) NOT NULL,
    porcentaje_aumentado DECIMAL(15, 2) NOT NULL,
    fecha_del_aumento    DATE           NOT NULL,
    FOREIGN KEY (fk_cui_empleado)
        REFERENCES empleado (cui_empleado)
);

